import "./Board.scss"

const data = [
  ["R", "", "", "", "", "", "", "", "", "", ""],
  ["", "R", "", "", "E", "", "", "", "", "", ""],
  ["", "", "R", "", "", "", "", "", "E", "", "R"],
  ["", "", "", "R", "", "P", "", "", "", "R", ""],
  ["", "", "", "", "R", "", "", "", "R", "", ""],
  ["", "", "E", "", "", "R", "", "R", "", "", ""],
  ["", "", "", "", "", "", "R", "", "", "", ""],
]

const Board = () => {
  return (
    <div id="board">
      {data.map((row, i) => (
        <div key={ i }>
          {row.map((val, j) => (
            <div key={ j }>
              {val === "" && "_"}
              {val === "R" && "^"}
              {val === "P" && "P"}
              {val === "E" && "E"}
            </div>
          ))}
        </div>
      ))}
    </div>
  );
};

export default Board;